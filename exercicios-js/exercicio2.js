// Minha resolução
function converterIdadeAnosParaDias(anos) {
    const diasParaAnos = anos * 365
    return diasParaAnos
}

console.log(converterIdadeAnosParaDias(17));

//  Resolução do Curso
function converterIdadeEmAnosParaDias(idadeEmAnos) {
    const diasDoAno = 365

    return diasDoAno * idadeEmAnos
}

console.log(converterIdadeAnosParaDias(17));